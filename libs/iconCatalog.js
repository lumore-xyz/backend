// Flat list of every curated icon, useful for client-side fuzzy search
export const IONICON_FLAT_LIST = Object.freeze(
  (() => {
    const seen = new Set();
    const out = [];
    for (const entry of Object.values(IONICON_CATALOG).flat()) {
      const key = `${entry.library}:${entry.name}`;
      if (seen.has(key)) continue;
      seen.add(key);
      out.push(entry);
    }
    return out;
  })(),
);

export const isKnownIoniconName = (name) =>
  IONICON_FLAT_LIST.some((entry) => entry.name === name);

export const isSupportedIconLibrary = (library) =>
  String(library || "").toLowerCase() === "ionicons";
